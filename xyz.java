public class xyz {
    public static void main(String[] args) {
        String fruit = "Apple";

        switch (fruit) {
            case "Apple":
                System.out.println("You chose an Apple!");
                break;
            case "Banana":
                System.out.println("You chose a Banana!");
                break;
            case "Orange":
                System.out.println("You chose an Orange!");
                break;
            default:
                System.out.println("Unknown fruit.");
        }
    }
}
